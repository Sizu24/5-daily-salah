      <div class="section-text js-basics-text" data-category="dua">
        <h3 class="section-text__title">What is Du'a?</h3>
        <p class="section-text__body">
          Du'a, or "supplication" refers to the act of communicating with Allah (God) through personal prayers and heartfelt expressions.
        </p>
        <p class="section-text__body">
          Du'a is a means through which Muslims seek guidance, blessings, forgiveness, mercy, and assistance from Allah in various
          aspects of their lives. It is an intimate conversation between an individual and their Creator, allowing them to express
          their deepest desires, concerns, and gratitude.
        </p>
        <p class="section-text__body">
          Muslims are encouraged to make Du'a in times of joy, sorrow, need, and gratitude.
        </p>
        <p class="section-text__body">
          It can be performed in any language, as
          Allah understands all languages, but using Arabic phrases and verses from the Quran is highly recommended.
        </p>
        <p class="section-text__body">
          Du'a can be made at any time and in any place, and does not require a specific ritual or physical posture. 
          It can be performed silently or verbally, individually or in congregation, 
          and can range from a simple personal plea to a comprehensive supplication encompassing various aspects of life.
        </p>
