        <h3 class="section-text__title">What is Wudu?</h3>
        <p class="section-text__body">
          Wudu, also known as ablution, is a purification process that Muslims perform before prayer. 
        </p>
        <p class="section-text__body">
          It involves the washing of specific body parts, symbolizing physical and spiritual cleansing. 
        <p class="section-text__body">
          Wudu is an essential prerequisite for Salah (prayer) as it prepares individuals to stand before Allah with a clean and focused state of
          mind.
        </p>
        <p class="section-text__body">
          See the <a href="" class="section-text__link">Wudu section</a> to learn how to perform Wudu before prayer.
        </p>
      </div>

      <div class="section-text js-basics-text" data-category="rakat">
        <h3 class="section-text__title">What is a Rakat?</h3>
        <p class="section-text__body">
          A Rakat refers to a unit of prayer. 
        </p>
        <p class="section-text__body">
          Each prayer consists of a specific number of Rakats, and the number varies depending on the type of prayer
          being offered.
        </p>
        <p class="section-text__body">
          Imagine you want to do pushups as a quick workout.
          You tell yourself you want to do 3 sets. 
        </p>
        <p class="section-text__body">
          Like a workout, your prayer has parts to it, called rakats. 
          A prayer usually has 2, 3, or 4 rakats, which are like the sets within the prayer. 
        </p>
      </div>

